package stack;

public class swapusing3variable {public static void main(String[] args) {
    int a=12;
    int b=10;
    int c;
    System.out.println("variables before swapping");
    System.out.println(a);
    System.out.println(b);
    c=a;
    a=b;
    b=c;
    System.out.println("variables after swapping");
    System.out.println(a);
    System.out.println(b);
    
}
    
}
